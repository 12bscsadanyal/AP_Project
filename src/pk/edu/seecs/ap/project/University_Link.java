package pk.edu.seecs.ap.project;

public class University_Link {
	
	private String Username;
	private String University_Links;
	
	public University_Link(){}

	public String getUsername() {
		return Username;
	}
	public void setUsername(String username) {
		Username = username;
	}
	public String getUniversity_Links() {
		return University_Links;
	}
	public void setUniversity_Links(String university_Links) {
		University_Links = university_Links;
	}	
}
