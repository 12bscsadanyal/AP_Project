package pk.edu.seecs.ap.project;

public class Forum {

	private String Forum_Title;
	private String Username;
	private String Forum_Date;
	private String Forum_Time;
	private String Category;
	
	public Forum(){}
	
	public String getForum_Title() {
		return Forum_Title;
	}
	public void setForum_Title(String forum_Title) {
		Forum_Title = forum_Title;
	}
	public String getUsername() {
		return Username;
	}
	public void setUsername(String username) {
		Username = username;
	}
	public String getForum_Date() {
		return Forum_Date;
	}
	public void setForum_Date(String forum_Date) {
		Forum_Date = forum_Date;
	}
	public String getForum_Time() {
		return Forum_Time;
	}
	public void setForum_Time(String forum_Time) {
		Forum_Time = forum_Time;
	}
	public String getCategory() {
		return Category;
	}
	public void setCategory(String category) {
		Category = category;
	}



	
}
