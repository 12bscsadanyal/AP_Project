package pk.edu.seecs.ap.project;

public class University_Info {
	
	private String University_Name;
	private String Location;
	private String campus;
	private String User_Ranking_Average;
	private String Number_Of_Rating; 
	private String Hostel;
	private String University_Type;
	
	public University_Info(){}

	public String getUniversity_Name() {
		return University_Name;
	}
	public void setUniversity_Name(String university_Name) {
		University_Name = university_Name;
	}
	public String getLocation() {
		return Location;
	}
	public void setLocation(String location) {
		Location = location;
	}
	public String getCampus() {
		return campus;
	}
	public void setCampus(String campus) {
		this.campus = campus;
	}
	public String getUser_Ranking_Average() {
		return User_Ranking_Average;
	}
	public void setUser_Ranking_Average(String user_Ranking_Average) {
		User_Ranking_Average = user_Ranking_Average;
	}
	public String getNumber_Of_Rating() {
		return Number_Of_Rating;
	}
	public void setNumber_Of_Rating(String number_Of_Rating) {
		Number_Of_Rating = number_Of_Rating;
	}
	public String getHostel() {
		return Hostel;
	}
	public void setHostel(String hostel) {
		Hostel = hostel;
	}
	public String getUniversity_Type() {
		return University_Type;
	}
	public void setUniversity_Type(String university_Type) {
		University_Type = university_Type;
	}
		
}
